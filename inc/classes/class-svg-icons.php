	/**
	 * Gets the SVG for a requested icon.
	 *
	 * @static
	 *
	 * @since 3.5.0
	 *
	 * @param string $icon      The name of the icon.
	 * @param int    $size      The icon size, in pixels.
	 * @param string $classname The class attribute(s).
	 * @return string The requested icon SVG HTML.
	 */
	public static function get_svg( string $icon, int $size, string $classname = '' ): string {
		$svg = '';
		if ( array_key_exists( $icon, self::$icons ) ) {
			$class = 'svg-icon ' . (string) $classname;
			$repl = sprintf(
				'<svg class="%2$s" width="%1$d" height="%1$d" aria-hidden="true" role="img" focusable="false" ',
				esc_attr( $size ),
				esc_attr( $class )
			);

			// Add extra attributes to SVG code.
			$svg = preg_replace( '/^<svg /', $repl, trim( self::$icons[ $icon ] ) );
		}

		return $svg;
	}
